export const createQuery = (query) => {
  return (dispatch) => {
    let data = {
      method: 'POST',
      url: `/queries/create`,
      data: formData(query)
    }

    return authApi(data).then(res => {
      if (res.status === 200) {
        return res
      } else {
        throw res
      }
    }).catch(err => {
      throw err
    })
  }
}
